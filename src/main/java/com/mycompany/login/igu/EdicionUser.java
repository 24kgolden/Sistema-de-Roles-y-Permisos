    //Metodo mostrar mensaje
    public void mostrarMensaje(String mensaje, String tipo, String titulo) {
        JOptionPane optionPane = new JOptionPane(mensaje);
        if (tipo.equals("Info")) {
            optionPane.setMessageType(JOptionPane.INFORMATION_MESSAGE);
        } else if (tipo.equals("Error")) {
            optionPane.setMessageType(JOptionPane.ERROR_MESSAGE);
        }
        JDialog dialog = optionPane.createDialog(titulo);
        dialog.setAlwaysOnTop(true);
        dialog.setVisible(true);
    }

    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        //buscar el usuario
        User usu = control.traerUser(id_user);
        //cargando datos en la interfas
        txtUser.setText(usu.getNameUser());
        txtPassword.setText(usu.getPassword());

        //cargar roles de forma generica
        List<Rol> listaRoles = control.traerRoles();

        if (listaRoles != null) {
            for (Rol listaRole : listaRoles) {
                cmbRol.addItem(listaRole.getNameRol());
            }
        }

        //marcar/seleccionar el rol que tiene este usuario 
        String rol = usu.getOneRol().getNameRol();

        int cantidadaItems = cmbRol.getItemCount();
        //recorremos el combo
        for (int i = 0; i < cantidadaItems; i++) {
            if (String.valueOf(cmbRol.getItemAt(i)).equals(rol)) {
                cmbRol.setSelectedIndex(i);
            }

        }
    }//GEN-LAST:event_formWindowOpened


